	private int getIndice(String tipo){
        for(int i = 0; i < tipos.length; i++){
            if(tipos[i].equalsIgnoreCase(tipo)){
                return i;
            }
        }
        return -1;
    }

    public double getEfectividad(String tipoJugador, String tipoRival){
        int fila = getIndice(tipoJugador);
        int columna = getIndice(tipoRival);

        if(fila == -1 || columna == -1){
            return 1.0;
        }

        return EFECTIVIDAD[fila][columna];
    }
